 * Enumeration for horizontal alignment. This is used to determine how the content of a window should be aligned horizontally.
 *
 * @package Atatusoft\Termutil\UI\Windows\Enumerations
 */
enum HorizontalAlignment
{
    case LEFT;
    case CENTER;
    case RIGHT;
}
